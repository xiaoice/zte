    public QQFaceDialog(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        initComponents();

        //defautFacePanel.removeAll();
        //表情加载
        int i = 0;
        JButton[] face = new JButton[105];
        for (int t = 0; t < 7; t++) {
            JToolBar toolBar = new JToolBar();
            toolBar.setFloatable(false);
            toolBar.setRollover(true);
            for (int j = 0; j < 15; j++, i++) {
                face[i] = new JButton();
                face[i].setToolTipText("~face:" + i + "~");
                face[i].setIcon(new ImageIcon(getClass().getResource("/iqq/res/images/face/" + i + ".gif"))); // NOI18N
                face[i].setFocusable(false);
                face[i].setHorizontalTextPosition(javax.swing.SwingConstants.CENTER);
                face[i].setVerticalTextPosition(javax.swing.SwingConstants.BOTTOM);
                toolBar.add(face[i]);

                //表情按钮事件
                face[i].addActionListener(new ActionListener() {

                    @Override
                    public void actionPerformed(ActionEvent e) {
                        JButton btn = (JButton) e.getSource();

                        Document doc = editor.getDocument();
                        try {
                            doc.insertString(editor.getSelectionEnd(), btn.getToolTipText(), null);
                        } catch (BadLocationException ex) {
                            Logger.getLogger(QQFaceDialog.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        String tipText = btn.getToolTipText();
                        Log.println(tipText + " flag:" + tipText.substring(6, tipText.lastIndexOf("~")));
                        editor.setText(editor.getText().replaceAll(tipText, QQImageUtil.flagToHTML(tipText.substring(6, tipText.lastIndexOf("~")))));
                        setVisible(false);
                    }
                });
            }
            defautFacePanel.add(toolBar);
            if (i == 104) {
                break;
            }
        }
        this.validate();
        this.repaint();
    }

