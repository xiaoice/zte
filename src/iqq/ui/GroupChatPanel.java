    private void initGroupMembers() {
        JLabel loading = new JLabel("加载中...", JLabel.CENTER);
        loading.setIcon(new ImageIcon(Toolkit.getDefaultToolkit().createImage(
                getClass().getResource("/iqq/res/images/icon/flash.gif"))));
        groupTreePanel.add(loading);
        Runnable r = new Runnable() {

            @Override
            public void run() {
                List<Member> memberList = GroupService.getInstance().downloadMemberList(group.getCode());

                if (memberList == null || memberList.isEmpty()) {
                    return;
                }
                Log.println("initGroupMembers size:" + memberList.size());
                DefaultMutableTreeNode root = new DefaultMutableTreeNode("Members");
                IconTreeNode itn = null;
                for (Member m : memberList) {
                    try {
                        itn = new IconTreeNode(m);
                    } catch (Exception ex) {
                        Logger.getLogger(MainPanel.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    root.add(itn);
                }
                groupMemberTree = new JTree(root);

                groupMemberTree.setRootVisible(true);
                DefaultTreeCellRenderer ceeRender = new MyDefaultTreeCellRenderer();
                groupMemberTree.setCellRenderer(ceeRender);
                groupTreePanel.removeAll();
                groupTreePanel.add(new JScrollPane(groupMemberTree));

            }
        };

        ThreadUtil.submit(r);

    }
