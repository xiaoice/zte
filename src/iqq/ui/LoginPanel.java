    private void toLogin(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_toLogin
        // TODO add your handling code here:
        String account = this.combAccount.getSelectedItem().toString();
        if(!FileLockUtil.getFileLock(account)) {
            ErrorMessage.show("QQ已经登录！ Account:" + account);
            return;
        }
        if (mainFrame.getMap() == null) {
            Map map = new HashMap();
            mainFrame.setMap(map);
        }

        Log.println("to login");
        Map loginMap = new HashMap();
        loginMap.put("account", account);
        loginMap.put("password", this.textPassword.getText().toString());
        loginMap.put("verifyCode", this.textPassword.getText().toString());
        loginMap.put("rememberPassword", this.RememberCheckBox.isSelected());
        int loginStatusIndex = this.comboLoginStatus.getSelectedIndex();
        String loginStatus = "online";
        switch (loginStatusIndex) {
            case 1: {
                loginStatus = "callme";
                break;
            }
            case 2: {
                loginStatus = "busy";
                break;
            }
            case 3: {
                loginStatus = "hidden";
                break;
            }
            case 4: {
                loginStatus = "silent";
                break;
            }
        }
        loginMap.put("loginStatus", loginStatus);

        LoginProcessPanel loginProcessPanel = new LoginProcessPanel();
        try {
            mainFrame.getMap().put("loginMap", loginMap);
            loginProcessPanel.login();
        } catch (Exception ex) {
            Logger.getLogger(LoginPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        mainFrame.getMap().put("loginPanel", this);
        mainFrame.setContentPane(loginProcessPanel);
        mainFrame.validate();

    }//GEN-LAST:event_toLogin

