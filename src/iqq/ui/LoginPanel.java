    private void lblAccountMenageMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblAccountMenageMouseClicked
        // TODO add your handling code here:
        JPopupMenu popupMenu = new JPopupMenu();
        JMenuItem deleteItem = new JMenuItem("删除账号");
        JMenuItem clearPasswordItem = new JMenuItem("清除密码");
        popupMenu.add(deleteItem);
        popupMenu.add(clearPasswordItem);
        Component c = (Component) evt.getSource();
        popupMenu.show(c, -1, c.getHeight());
        deleteItem.addActionListener(new ActionListener() {

            @Override
            public void actionPerformed(ActionEvent e) {
                String account = (String) combAccount.getSelectedItem();
                combAccount.removeItemAt(combAccount.getSelectedIndex());
                Map m = new HashMap();
                m.put("account", account);
                PersistenceUtils.getInstance().update(m);
            }
        });

        clearPasswordItem.addActionListener(new ActionListener() {

            @Override
            public void actionPerformed(ActionEvent e) {
                String account = (String) combAccount.getSelectedItem();
                combAccount.removeItemAt(combAccount.getSelectedIndex());
                Map m = new HashMap();
                m.put("account", account);
                m.put("password", "");
                PersistenceUtils.getInstance().update(m);
            }
        });
    }//GEN-LAST:event_lblAccountMenageMouseClicked

