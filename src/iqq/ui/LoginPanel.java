    private void combAccountItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_combAccountItemStateChanged
        // TODO add your handling code here:
        String account = this.combAccount.getSelectedItem().toString();
        try {
            if (memberList != null && !memberList.isNull(account)) {
                this.textPassword.setText(memberList.getString(account));
            }
        } catch (JSONException ex) {
            Logger.getLogger(LoginPanel.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_combAccountItemStateChanged

